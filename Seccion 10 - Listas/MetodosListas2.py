lista = [5 , 3 , 1 , 4 , 2]

print(lista.count(5)) #Cuenta la cantidad de veces que aparece el elemento 5 en la lista 
print(lista.index(4)) #Imprime la posicion del elemento 4 en la lista 
print(lista) #Imprime la lista
lista.sort() #Ordena la lista de menor a mayor 
print(lista)
lista.reverse() #Invierte la lista 
print(lista)