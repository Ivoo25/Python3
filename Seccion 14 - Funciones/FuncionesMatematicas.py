import math # Importa la libreria math para usar la funcion sqrt, pow, etc
import random # Importa la libreria random para usar la funcion randint, etc

print(pow(10 , 2)) # 10 elevado a 2, este pow no es de la libreria math
print(math.sqrt(64)) # Raiz cuadrada de 64, pero como flotante
print(math.isqrt(64)) # Raiz cuadrada de 64, pero como entero
print(math.sin(90)) # Seno de 90
print(math.cos(90)) # Coseno de 90
print(math.tan(90)) # Tangente de 90
print(math.factorial(10)) # Factorial de 10

print(random.randint(1 , 100)) # Numero aleatorio entre 1 y 100